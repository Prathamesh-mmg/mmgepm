    private readonly AppDbContext _db;
    private readonly ILogger<DprService> _logger;

    public DprService(AppDbContext db, ILogger<DprService> logger)
    { _db = db; _logger = logger; }

    public async Task<DprCompiledData> CompileDprSectionsAsync(Guid projectId, DateTime date)
    {
        var targetDate = date.Date;

        // 1. Work Progress — tasks updated today
        var workProgress = await _db.WorkProgress
            .Include(wp => wp.Task)
            .Include(wp => wp.UpdatedBy)
            .Where(wp => wp.TaskId != null
                && wp.Task.ProjectId == projectId
                && wp.ReportedAt.Date == targetDate
                && !wp.IsDeleted)
            .Select(wp => new DprTaskSection(
                wp.Task.Name,
                wp.ProgressPercentage,
                wp.Task.Status,
                wp.Notes))
            .ToListAsync();

        // 2. Delays — logged today
        var delays = await _db.TaskDelays
            .Include(d => d.Task)
            .Where(d => d.Task.ProjectId == projectId
                && d.CreatedAt.Date == targetDate
                && !d.IsDeleted)
            .Select(d => new DprDelaySection(
                d.Task.Name, d.DelayType, d.DelayHours, d.Description))
            .ToListAsync();

        // 3. Labour — approved attendance today
        var labourEntries = await _db.CrewAttendance
            .Where(a => a.ProjectId == projectId
                && a.AttendanceDate.Date == targetDate
                && a.ApprovalStatus == "Approved"
                && !a.IsDeleted)
            .ToListAsync();

        var labour = labourEntries
            .GroupBy(a => a.TradeName ?? "General")
            .Select(g => new DprLabourSection(
                g.Key,
                g.Sum(a => a.PlannedCount ?? 0),
                g.Sum(a => a.ActualCount  ?? 0),
                g.Sum(a => (a.ActualCount ?? 0) - (a.PlannedCount ?? 0))))
            .ToList();

        // 4. High/Critical risks — from project
        var risks = await _db.Risks
            .Where(r => r.ProjectId == projectId
                && (r.RiskLevel == "High" || r.RiskLevel == "Critical")
                && r.Status != "Closed"
                && !r.IsDeleted)
            .Include(r => r.RiskOwner)
            .Select(r => new DprRiskSection(
                r.RiskNumber, r.Title, r.RiskLevel ?? "High",
                r.RiskOwner != null ? r.RiskOwner.FullName : null))
            .Take(10)
            .ToListAsync();

        var totalLabour    = labourEntries.Sum(a => a.ActualCount  ?? 0);
        var totalEquipment = await _db.TaskResourceAllocations
            .Include(a => a.Resource).ThenInclude(r => r.ResourceType)
            .CountAsync(a => a.Task.ProjectId == projectId
                && a.StartDate.Date <= targetDate
                && a.EndDate.Date >= targetDate
                && (a.Resource.ResourceType != null && a.Resource.ResourceType.Category == "Equipment")
                && !a.IsDeleted);

        return new DprCompiledData(workProgress, delays, labour, risks, totalLabour, totalEquipment);
    }

