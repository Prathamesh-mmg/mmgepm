namespace MMG.EPM.API.Infrastructure.Services;

// ─── DPR Service Interface ─────────────────────────────────────

public interface IDprService
{
    Task<DprCompiledData> CompileDprSectionsAsync(Guid projectId, DateTime date);
    string GenerateDprHtml(DPRReport dpr, DprCompiledData data);
    Task AutoGenerateDailyDprsAsync();
}

public record DprCompiledData(
    List<DprTaskSection>  WorkProgress,
    List<DprDelaySection> Delays,
    List<DprLabourSection> Labour,
    List<DprRiskSection>  Risks,
    int TotalLabour, int TotalEquipment);

// ─── DPR Service Implementation ────────────────────────────────

public class DprService : IDprService
