    // Called by Hangfire CRON at 11AM daily
    public async Task AutoGenerateDailyDprsAsync()
    {
        _logger.LogInformation("Auto-generating DPRs for {Date}", DateTime.Today);

        var activeProjects = await _db.Projects
            .Where(p => p.Status == "Active" && !p.IsDeleted)
            .Select(p => p.Id).ToListAsync();

        var count = 0;
        foreach (var pid in activeProjects)
        {
            // Skip if already exists
            var exists = await _db.DPRReports.AnyAsync(d =>
                d.ProjectId == pid && d.ReportDate.Date == DateTime.Today && !d.IsDeleted);
            if (exists) continue;

            _db.DPRReports.Add(new DPRReport
            {
                ProjectId       = pid,
                ReportDate      = DateTime.Today,
                Status          = "Draft",
                IsAutoGenerated = true,
                WeatherType     = "Normal",
                WeatherCondition = "Clear",
            });
            count++;
        }

        if (count > 0)
        {
            await _db.SaveChangesAsync();
            _logger.LogInformation("Auto-generated {Count} DPR shells", count);
        }
    }
